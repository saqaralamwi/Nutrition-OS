export function getAdolescentRefData(
  gender: 'male' | 'female',
  indicator: 'bmifa' | 'lhfa' | 'wfa' = 'bmifa',
): WhoLmsPoint[] {
  if (indicator === 'lhfa') {
    return gender === 'male' ? ADOLESCENT_LHFA_MALES : ADOLESCENT_LHFA_FEMALES;
  }
  if (indicator === 'wfa') {
    return gender === 'male' ? ADOLESCENT_WFA_MALES : ADOLESCENT_WFA_FEMALES;
  }
  return gender === 'male' ? ADOLESCENT_BMIFA_MALES : ADOLESCENT_BMIFA_FEMALES;
}

export function isAdolescentIndicatorSupported(
  indicator: 'wfa' | 'lhfa' | 'bmifa',
  ageMonths: number,
): boolean {
  if (indicator === 'bmifa') return ageMonths >= 61 && ageMonths <= 228;
  if (indicator === 'lhfa') return ageMonths >= 61 && ageMonths <= 228;
  if (indicator === 'wfa') return ageMonths >= 61 && ageMonths <= 120;
  return false;
}
